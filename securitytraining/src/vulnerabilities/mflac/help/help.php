                        <p>Virtually all web applications verify function level access rights before making that
                            functionality visible in the UI.
                            <br>However, applications need to perform the same access control checks on the server when
                            each function is accessed.
                            If requests are not verified, attackers will be able to forge requests in order to access
                            unauthorized functionality.</p>
